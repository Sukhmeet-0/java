public class Literals {// a constant value which can be aassigned to a variable is called literals
    public static void main(String[] args) {
        byte age=20;
        System.out.println(age);
        //for char use single quotes
        //for string use double quotes
        float f=5.6f;//f is for floating number as default value is double "f " is a literal d is used for double L for long 
        System.out.println(f);
    }
}
